const selectCat = document.getElementById("categoria");
const btnVer = document.getElementById("ver");
const listaSection = document.getElementById("listaSection");
const lista = document.getElementById("lista");
const catTitulo = document.getElementById("catTitulo");
const lecturaSection = document.getElementById("lectura");
const tituloLectura = document.getElementById("tituloLectura");
const textoLectura = document.getElementById("textoLectura");
const btnCerrar = document.getElementById("cerrar");

btnVer.addEventListener("click", mostrarLibros);
btnCerrar.addEventListener("click", cerrarLectura);

function mostrarLibros() {
  const cat = selectCat.value;
  const items = biblioteca[cat];
  catTitulo.textContent = {
    literatura: "Lengua y Literatura",
    historia: "Historia",
    ciencias: "Ciencias Naturales",
    valores: "Valores y convivencia",
    ecuador: "Cultura de Ecuador"
  }[cat];
  lista.innerHTML = "";
  items.forEach((libro, i) => {
    const li = document.createElement("li");
    li.innerHTML = `<strong>${escapeHtml(libro.titulo)}</strong>
      <button class="leer-btn" onclick="leer('${cat}', ${i})">Leer</button>
      <div style="color:#4b5563; font-size:0.95em; margin-top:6px;">(Lectura larga)</div>`;
    lista.appendChild(li);
  });
  listaSection.style.display = "block";
  lecturaSection.style.display = "none";
}

function leer(cat, index) {
  const libro = biblioteca[cat][index];
  tituloLectura.textContent = libro.titulo;
  textoLectura.textContent = libro.texto;
  lecturaSection.style.display = "block";
  window.scrollTo({ top: 0, behavior: 'smooth' });
}

function cerrarLectura() {
  lecturaSection.style.display = "none";
}

function escapeHtml(s) {
  if (!s && s !== 0) return "";
  return String(s).replace(/[&<>"']/g, function (m) {
    return {'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[m];
  });
}

/* Mostrar al cargar la primera categoría por defecto */
document.addEventListener("DOMContentLoaded", () => {
  mostrarLibros();
});
